function renderMarker($city, $country, $ipAddress, $latitude, $longitude, $type='visitor') {
    $output = "
    <marker
    city=\"$city\"
    country=\"$country\"
    address=\"$ipAddress\"
    lat=\"$latitude\"
    lng=\"$longitude\"
    type=\"$type\"/>";
    return $output;
}

include_once JPATH_BASE2 . DS . "components" . DS . "com_extrawatch" . DS. "includes.php";  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
