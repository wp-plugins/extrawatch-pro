      die($reason);
    }
    $token = $this->config->getTokenFromRequestUrl();
    if (!$this->config->isPermittedWithBackendToken($token)) {
      $this->blockIp($ip, $reason, $this->date->jwDateToday(), @constant(_EW_BLOCKING_UNAUTHORIZED_ACCESS));  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
      die($reason);
    }
  }

