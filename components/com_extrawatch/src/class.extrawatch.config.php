  function getFrontendToken()
  {
    return sha1(sha1(gmdate("d w")).sha1($this->getConfigValue("rand")));
  }

  function getHeatmapToken()
  {
    return sha1(gmdate("d w").sha1(sha1($this->getConfigValue("rand"))));
  }

