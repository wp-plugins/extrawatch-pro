    /**
     * @param $this
     */
    function saveRandValue()
    {
        $rand = $this->getConfigValue('rand');
        if (!$rand) {
            $this->saveConfigValue('rand', sha1(sha1(mt_rand()) + mt_rand()));
        }
    }

