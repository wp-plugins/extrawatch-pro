    function checkIfHtAccessTxtPresentOnJoomla() {

        if ($this->env->getEnvironmentName() == "joomla") {
            $rootPath = $this->env->getRootPath();
            $htaccess_file = $rootPath.DS."htaccess.txt";
            if (@file_exists($htaccess_file)) {
                return TRUE;
            }
            return FALSE;
        }
    }
	
