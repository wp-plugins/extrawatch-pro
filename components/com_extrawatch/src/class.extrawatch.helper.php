	static function getUrlContent($url) {	
		if (@function_exists("curl_init")) {
			$ch = curl_init();
		
			curl_setopt_array(
				$ch, array(
				CURLOPT_URL => ExtraWatchInput::validate(_EW_INPUT_URL, $url), ///
				CURLOPT_RETURNTRANSFER => true
			));
	
			$content = curl_exec($ch);
 
			curl_close($ch);
			return $content;
		}
	}


