  {

    $query = sprintf("select id as maxid from #__extrawatch where browser is not NULL order by id desc limit 1");
    $rows = @ $this->database->objectListQuery($query);  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
