   * @return
   */
  function extractPhraseFromUrl($referer)  	 	///
  {
    if (@strstr($referer, "usg=") && @strstr($referer, "q=&")) {  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
		if ($this->config->getCheckboxValue("EXTRAWATCH_SEO_LIST_ENCRYPTED_KEYWORDS")) {  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
			return ExtraWatchHelper::_EW_ENCRYPTED;  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
		}
    }
    if (@strstr($referer, "q=")) { // keywords from google  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
      preg_match('![&|?]q=([^&]*)!', urldecode($referer), $matches);  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
	  $keyphrase = trim(strtolower(@$matches[1]));  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
	  ExtraWatchLog::debug("Keyphrase found from url: $referer: $keyphrase");  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
    return $keyphrase;  	 	    	    		  	 	  	 	  		 	 		    	 			 	   		  	 	 		 	 	   	      	  	 		 		 				 			 		  		    	 		 		  
    }
	return;
  }

