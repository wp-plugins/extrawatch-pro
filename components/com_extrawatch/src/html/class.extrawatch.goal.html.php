  {
    $output = ExtraWatchHelper::get_include_contents("view" . DS . "goal.php", array("extraWatch"=>$this->extraWatch, "extraWatchHTML"=>$this->extraWatchHTML, "action"=>$action, "values"=>$values));
    return $output;
  }

